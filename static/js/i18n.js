let currentLang = localStorage.getItem('cafe_lang') || 'en';

function t(key) {
  return LANG[currentLang][key] || key;
}

function toggleLang() {
  currentLang = currentLang === 'en' ? 'th' : 'en';
  localStorage.setItem('cafe_lang', currentLang);
  document.getElementById('lang-toggle').textContent = t('lang.' + currentLang);
  translateStatic();
  const page = document.querySelector('.topbar-nav a.active')?.dataset?.page || 'dashboard';
  navigate(page);
}

function translateStatic() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.placeholder !== undefined) el.placeholder = t(key);
    } else if (el.tagName === 'SELECT') {
      const optKey = el.dataset.i18nOpt;
      if (optKey && el.options.length > 1) {
        for (let i = 0; i < el.options.length; i++) {
          const ok = optKey.split(',');
          if (ok[i]) el.options[i].textContent = t(ok[i]);
        }
      }
    } else if (el.tagName === 'OPTION') {
      el.textContent = t(key);
    } else {
      el.textContent = t(key);
    }
  });
}
